#include "libft.h"

t_list	*ft_lstlast(t_list *lst)
{
	t_list	*aux;

	aux = lst;
	if (!aux)
		return (NULL);
	while (aux->next != NULL)
	{
		aux = aux->next;
	}
	return (aux);
}
