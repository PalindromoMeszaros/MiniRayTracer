#include "libft.h"

size_t	ft_strlcpy(char *dst, const char *src, size_t dstsize)
{
	size_t	i;
	size_t	s_size;

	s_size = 0;
	i = 0;
	if (!dst)
		return (0);
	while (src[s_size] != '\0')
	{
		s_size++;
	}
	if (!dstsize)
		return (s_size);
	while (src[i] != '\0' && i < (dstsize - 1))
	{
		dst[i] = src[i];
		i++;
	}
	if (dstsize != 0)
	{
		dst[i] = '\0';
	}
	return (s_size);
}
